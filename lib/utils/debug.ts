/**
 * Debug logging utility
 * Only logs in development mode
 */

const isDev = process.env.NODE_ENV === "development";

export const debugLog = {
  log: (...args: any[]) => {
    if (isDev) {
      console.log(...args);
    }
  },
  warn: (...args: any[]) => {
    if (isDev) {
      console.warn(...args);
    }
  },
  error: (...args: any[]) => {
    // Always log errors, even in production
    console.error(...args);
  },
};

